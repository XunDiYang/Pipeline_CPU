.org 0x0
.global _start
.set noat

#test01: 测试逻辑操作实现效果
#expect result:
# $1 0x00000010
# $2 0x01011101
# $3 0xfefeeeee
# $4 0x00000010

_start:
	lui $1, 0x0101			# $1 = 0x01010000
	ori $1, $1, 0x0101		# $1 = $1 | 0x0101 = 0x01010101
	ori $2, $1, 0x1100		# $2 = $1 | 0x1100 = 0x01011101
	or $1, $1, $2			# $1 = $1 | $2 = 0x01011101
	andi $3, $1, 0x00fe		# $3 = $1 & 0x00fe = 0x00000000
	and $1, $3, $1			# $1 = $3 & $1 = 0x00000000
	xori $4, $1, 0x0010		# $4 = $1 ^ 0xff00 = 0x00000010
	xor $1, $4, $1			# $1= $4 ^ $1 = 0x00000010
	nor $3, $4, $2			# $3 = $4 ~^ $2 = 0xfefeeeee